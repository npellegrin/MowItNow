package fr.npellegrin.xebia.mower.parser.model;

public class YardDefinition {
	private int lastCoordX;
	private int lastCoordY;

	public int getLastCoordX() {
		return lastCoordX;
	}

	public void setLastCoordX(final int lastCoordX) {
		this.lastCoordX = lastCoordX;
	}

	public int getLastCoordY() {
		return lastCoordY;
	}

	public void setLastCoordY(final int lastCoordY) {
		this.lastCoordY = lastCoordY;
	}

}
